#include "3-calc.h"
/**
 * op_add - returns the additon of two numbers
 * @a: first number to add
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference of two numbers
 * @a: first number
 * @b: second number
 * Return: diff
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product of two numbers
 * @a: first number
 * @b: second number
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns result of the div of2 nos.
 * @a: first number
 * @b: second number
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the remainder of the div of 2 nos.
 * @a: first number
 * @b: second number
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
